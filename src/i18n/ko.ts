// leaf 의 한글 literal 타입을 string 으로 풀어줌. en.ts 가 같은 키 구조만 충족하면 통과
// (값은 영문 string 이면 OK). 누락된 키는 컴파일 에러로 검출.
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string ? string : DeepStringify<T[K]>;
};

